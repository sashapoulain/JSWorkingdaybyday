//switch case


const num = 5;
switch (num) {
    case 1:
        console.log('sayı 1');
        break;
    case 3:
        console.log('satyı 3');
        break;
    case 5:
        console.log('sayı 5');
        break;
    default:
        console.log('geçersiz sayı');
}